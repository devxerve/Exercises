    private void jComboBoxUnidadesfinalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxUnidadesfinalesActionPerformed
       int opcion = (Integer)jComboBoxUnidadesIniciales.getSelectedIndex();  
       if (!jTextFieldValorInicial.getText().trim().equals("0"))
         switch (opcion) {
           case 0:
             break;
           case 1:
            convertirDesdeMl();
            break;
          case 2:
            convertirDesdeCl();
            break;
          case 3:
            convertirDesdeLt();
            break;
          default:
            break;
         }
    }//GEN-LAST:event_jComboBoxUnidadesfinalesActionPerformed

