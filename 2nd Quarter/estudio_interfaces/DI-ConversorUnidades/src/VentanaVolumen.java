    private void limpiarVentana() {
        jTextFieldValorInicial.setText("0");
        colorjLabelResultadoON();
        jLabelResultado.setText("0");
        jComboBoxUnidadesIniciales.setSelectedIndex(0);
        jComboBoxUnidadesfinales.setSelectedIndex(0);
   }
    
     private void colorjLabelResultadoON() {
    
        jLabelResultado.setOpaque(true); // Habilita el fondo que por defecto es transparente
        jLabelResultado.setBackground(Color.RED); // Cambia el color de fondo
        jLabelResultado.setForeground(Color.WHITE); // Color del texto
    }
   
     private void calcularResultado(Double valor, Double factorConversion, char format) {
        valor=valor*factorConversion;
        jLabelResultado.setText(String.format("%."+format+"f", valor));
    }
    
     
    private void convertirDesdeMl() {
        Double valor;

        int opcion = (Integer)jComboBoxUnidadesfinales.getSelectedIndex();


       switch (opcion) {
          case 0:
            break;
          case 1:
            jLabelResultado.setText(jTextFieldValorInicial.getText());
            break;
          case 2:
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 0.1,'2');
            break;
          case 3:
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 0.001,'3');
            break;
          default:
            System.out.println("Opción no válida 2");
        }
    }
    
    private void convertirDesdeCl() {
        Double valor;

        int opcion = (Integer)jComboBoxUnidadesfinales.getSelectedIndex();
        
    // String opcion = (String) jComboBoxUnidadesfinales.getSelectedItem();
    
        switch (opcion) {
          case 0: //case "no seleccionado"
            break;
          case 1:  // case "mililitros"
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 10.0,'0');
            break;
          case 2: // case "centilitros"
            jLabelResultado.setText(jTextFieldValorInicial.getText());
            break;
          case 3: // case "litros"
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 0.01,'2');
            break;
          default:
            System.out.println("Opción no válida 2");
        }
    
    }
    
  private void convertirDesdeLt() {
        Double valor;

        int opcion = (Integer)jComboBoxUnidadesfinales.getSelectedIndex();
        switch (opcion) {
          case 0:
            break;
          case 1:
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 1000.0,'0');
            break;
          case 2:
            valor = Double.parseDouble(jTextFieldValorInicial.getText());
            calcularResultado(valor, 100.0,'0');
            break;
          case 3:
            jLabelResultado.setText(jTextFieldValorInicial.getText());
            break;
          default:
            break;
        }    
    
    }
   
