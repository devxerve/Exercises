    private void jButtonContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonContinuarActionPerformed
        // TODO add your handling code here:
        String seleccion = String.valueOf(jComboBoxOperaciones.getSelectedItem());
        String primero = jTextFieldPrimero.getText();
        String segundo = jTextFieldSegundo.getText();
        if(!seleccion.equals( "Selecciona una operacion")){
            if((primero.isBlank())&& (segundo.isBlank()) ){
                javax.swing.JOptionPane.showMessageDialog(this, "No hay operandos", "Operación sin operandos", javax.swing.JOptionPane.WARNING_MESSAGE);
            }
            else if(primero.isBlank()){
                javax.swing.JOptionPane.showMessageDialog(this, "El primer operando esta vacio", "Operación sin operando", javax.swing.JOptionPane.WARNING_MESSAGE);
            }else if(segundo.isBlank()){
                javax.swing.JOptionPane.showMessageDialog(this, "El segundo operando esta vacio", "Operación sin operando", javax.swing.JOptionPane.WARNING_MESSAGE);
            }
            operaciones(Integer.parseInt(primero), Integer.parseInt(segundo), String.valueOf(jComboBoxOperaciones.getSelectedItem()));
        }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una operacion primero", "Operación sin seleccionar", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonContinuarActionPerformed

