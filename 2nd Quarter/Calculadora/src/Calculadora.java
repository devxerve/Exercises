    public void operaciones(int num1, int num2, String operacion) {
        int resultado = 0;
        if (operacion == "+" ) {
            resultado = num1 + num2;
        }
        else if (operacion == "-") {
            resultado = num1 - num2;
        }
        jLabelResultado.setText("Resultado: " + resultado);
    }
    
